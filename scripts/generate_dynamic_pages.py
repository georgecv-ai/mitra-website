def media_block(hero: dict) -> str:
    image = hero.get("image")
    if image:
        ratio = aspect_css(hero.get("aspect"))
        alt = esc_attr(hero.get("image_alt", ""))
        return (
            '<div class="adl-hero-media">\n'
            f'        <div class="adl-adframe" style="aspect-ratio: {ratio};">\n'
            f'          <img src="{esc_attr(image)}" alt="{alt}" '
            'onerror="this.onerror=null;this.src=\'/en/static/images/mitra-logo-icon-v2_003.png\';this.style.objectFit=\'contain\';this.style.padding=\'26%\';this.style.opacity=\'.5\';">\n'
            '        </div>\n'
            '      </div>'
        )
    return (
        '<div class="adl-hero-media">\n'
        '        <div class="adl-adslot" role="img" aria-label="Ad creative placeholder">\n'
        '          <span class="adl-adslot-ico">&#128247;</span>\n'
        '          <span class="adl-adslot-label">AD CREATIVE</span>\n'
        '          <span class="adl-adslot-dims">the exact graphic from the ad goes here</span>\n'
        '        </div>\n'
        '      </div>'
    )


def render_page(entry: dict) -> str:
    hero = entry.get("hero", {})
    cta = entry.get("cta", {})
    meta = entry.get("meta", {})
    slug = entry["slug"]

    cta_href = build_cta_href(cta, entry.get("utm", {}))
    title = meta.get("title", "Mitra — start your free trial").replace("\n", " ")
    desc = meta.get("description", "").replace("\n", " ")

    text_block = (
        '<div class="adl-hero-text">\n'
        f'        <h1 class="adl-h1">{hero.get("headline_html", "")}</h1>\n'
        f'        <p class="adl-sub">{hero.get("subtext_html", "")}</p>\n'
        f'        <a class="adl-cta" href="{cta_href}" data-cta data-dest="hero">{esc_attr(cta.get("label", "Start your 7-day free trial"))}</a>\n'
        '        <p class="adl-note">No app to install &middot; works on the chat apps you already use</p>\n'
        '      </div>'
    )

    page = f"""<!-- META
title: {title}
description: {desc}
slug: dynamic/{slug}.html
main_class: adl-main
og_description: {desc}
-->
{GEN_MARKER}
<div class="adl-page">
  <section class="adl-hero">
    <div class="adl-hero-grid">
      {text_block}
      {media_block(hero)}
    </div>
  </section>
</div>
{ATTR_SCRIPT.replace("__SLUG__", slug)}"""
    return page


def main() -> int:
    if not DATA_PATH.exists():
        print(f"ERROR: {DATA_PATH} not found", file=sys.stderr)
        return 1
    data = json.loads(DATA_PATH.read_text(encoding="utf-8"))
    landings = data.get("landings", [])

    written = 0
    seen = set()
    for entry in landings:
        if entry.get("status") != "active":
            continue
        slug = entry.get("slug")
        lang = entry.get("lang", "en")
        if not slug:
            print("WARNING: skipping entry with no slug", file=sys.stderr)
            continue
        key = (lang, slug)
        if key in seen:
            print(f"ERROR: duplicate slug '{slug}' for lang '{lang}'", file=sys.stderr)
            return 1
        seen.add(key)

        out_dir = SRC / lang / "dynamic"
        out_dir.mkdir(parents=True, exist_ok=True)
        (out_dir / f"{slug}.html").write_text(render_page(entry), encoding="utf-8")
        (out_dir / f"{slug}.head.html").write_text(HEAD_CSS, encoding="utf-8")
        written += 1
        print(f"  wrote src/{lang}/dynamic/{slug}.html (+ .head.html)")

    print(f"Generated {written} dynamic landing page(s). Next: python build.py")
    return 0


if __name__ == "__main__":
    sys.exit(main())
