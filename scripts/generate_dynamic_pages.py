"""
Dynamic-landing generator — the "content-linked dynamic landing" building block.

Reads data/dynamic.json (spec: data/dynamic.schema.json), and for every `active`
landing emits a page fragment + head sidecar into src/<lang>/dynamic/<slug>.html.
Then build.py assembles them into en/dynamic/<slug>.html.

This is the reusable pattern: ONE template (below), MANY data entries, produced by
content-factory and consumed here. To add a landing, content-factory adds an entry
to the manifest — no HTML is hand-written.

  Workflow:
    python scripts/generate_dynamic_pages.py     # data -> src/<lang>/dynamic/*.html
    python build.py --env staging                # src -> en/dynamic/*.html

Per ad, only three slots change: the hero image (the creative), the headline, and
the subtext. Everything else is shared boilerplate owned here.
"""

import json
import sys
from pathlib import Path
from urllib.parse import quote

ROOT = Path(__file__).resolve().parent.parent
DATA_PATH = ROOT / "data" / "dynamic.json"
SRC = ROOT / "src"

# Marks generated files so a future clean step can remove stale pages safely.
GEN_MARKER = "<!-- generated by scripts/generate_dynamic_pages.py — do not edit by hand -->"


def aspect_css(value: str) -> str:
    v = (value or "1:1").replace(" ", "")
    if ":" in v:
        w, h = v.split(":", 1)
        return f"{w} / {h}"
    return "1 / 1"


def esc_attr(s: str) -> str:
    return (str(s or "")
            .replace("&", "&amp;").replace('"', "&quot;")
            .replace("<", "&lt;").replace(">", "&gt;"))


def build_cta_href(cta: dict, utm: dict) -> str:
    href = cta.get("href") or "{{app_base}}/en/enroll"
    parts = []
    for key in ("source", "medium", "campaign", "content", "term"):
        val = (utm or {}).get(key)
        if val:
            parts.append(f"utm_{key}={quote(str(val))}")
    if parts:
        href += ("&" if "?" in href else "?") + "&".join(parts)
    return href


